"""
==  equal
!=  not equal
<   less than
>   greater than
<=  less than or equal to
>=  greater than or equal to
All of them return boolean
"""

bull = 10 == 10
print('10 == 10 is', bull)

bull = 10 != 10
print('10 != 10 is', bull)

bull = 10 < 10
print('10 < 10 is', bull)

bull = 10 < 11
print('10 < 11 is', bull)

bull = 10 > 10
print('10 > 10 is', bull)

bull = 10 > 9
print('10 > 9 is', bull)

bull = 10 >= 10
print('10 >= 10 is', bull)

bull = 10 <= 10
print('10 <= 10 is', bull)

bull = 10 >= 11
print('10 >= 11 is', bull)

bull = 10 <= 9
print('10 <= 9 is', bull)
